int	ft_isspace(int c)
{
	unsigned char	cc;

	cc = (unsigned char )c;
	if (cc == ' ' || cc == '\f'
		|| cc == '\n' || cc == '\r'
		|| cc == '\t' || cc == '\v')
		return (1);
	return (0);
}
